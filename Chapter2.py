# 8월 4일 월요일 화이팅입니다!!!
name = input("이름을 입력하세요: ")  # 사용자로부터 이름 입력 받기
age = input("나이를 입력하세요: ")  # 사용자로부터 나이 입력 받기
print(type(age)) #input으로 입력된 값은 모두 문자열 타입
age = int(age)
print(type(age))


#문자열 포매팅 - 중괄호 안에 변수 넣기
print("안녕하세요, {}님! 당신의 나이는 {}세입니다.".format(name, age))  # format 메서드 사용
print("안녕하세요, %s님! 당신의 나이는 %d세입니다." % (name, age))  # % 연산자 사용
print(f"안녕하세요, {name}님! 당신의 나이는 {age}세입니다.")  # f-string 사용

#정수 포매팅
print("나이는 {:d}세입니다.".format(age))  # 정수 포매팅 - 중괄호 안에 :xxd 사용
print("개수는 {:4d}개입니다.".format(5))  # 4자리 정수, 빈 자리는 공백으로 채움
print("개수는 {:04d}개입니다.".format(5))  # 4자리로 출력, 빈 자리는 0으로 채움
print("개수는 {:,d}개입니다.".format(10000))  # 천 단위 구분 기호 사용
print("개수는 {:+5d}개입니다.".format(100))  # 양수는 + 기호로 표시
print("개수는 {:=+5d}개입니다.".format(-100))  #기호를 좌로 밀고 공백으로 채우기
print("개수는 {:+05d}개입니다.".format(100))  # 양수는 + 기호로 표시, 빈 자리는 0으로 채움

print("나이는 %d세입니다." % age)  # % 연산자 사용
print("개수는 %4d개입니다." % 10)  # 4자리 정수, 빈 자리는 공백으로 채움
print("개수는 %04d개입니다." % 10)  # 4자리 정수, 빈 자리는 0으로 채움
#이하 생략
print(f"나이는 {age:d}세입니다.")  # f-string 정수 포매팅

#실수 포매팅
print("실수는 {:.1f}입니다.".format(3.14159))  # 소수점 첫째 자리까지 포매팅
print("강수 확률은 {:.2f}%입니다.".format(75.1234))  # 소수점 둘째 자리까지 포매팅
print("실수는 {:10.2f}입니다.".format(3.14159))  # 전체 10자리, 소수점 둘째 자리까지
print("실수는 {:010.2f}입니다.".format(3.14159))  # 전체 10자리, 빈 자리는 0으로 채움
print("실수는 {:+10.2f}입니다.".format(3.14159))  # 양수는 + 기호로 표시, 전체 10자리, 소수점 둘째 자리까지
print("실수는 {:+010.2f}입니다.".format(3.14159))  # 양수는 + 기호로 표시, 전체 10자리, 빈 자리는 0으로 채움
print("실수는 {:=+10.2f}입니다.".format(-3.14159))  # 기호를 좌로 밀고 전체 10자리, 소수점 둘째 자리까지
print("실수는 %10.2f입니다." % 3.14159)  # % 연산자 사용
