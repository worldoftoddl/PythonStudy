print(ㅁㅈㅇ.rstrip())  # 오른쪽 공백 제거

print(input("문자열을 입력하세요: ").strip())  #입력된 값의 앞뒤 공백 제거

print(ㅁㅈㅇ.find("o"))
print(ㅁㅈㅇ.index("o"))  # "o"의 위치 찾기
print(ㅁㅈㅇ.rfind("o"))  # "o"의 마지막 위치 찾기
print(ㅁㅈㅇ.rindex("o"))  # "o"의 마지막 위치 찾기 (예외 발생 가능)

print(ㅁㅈㅇ in "Hello")  # "Hello"가 포함되어 있는지 확인

